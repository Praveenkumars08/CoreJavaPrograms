package com.java.basics;

public class Example04_IfElseConcept01 {
	
	public static void main(String[] args) {
		
		if(10==20) {
			System.out.println("Both the numbers are equal");
		}
		
		else {
			System.out.println("Both the numbers are not equal");
		}
		
	}

}

